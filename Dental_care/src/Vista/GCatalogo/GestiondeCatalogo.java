    private void BuscarTodoCKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BuscarTodoCKeyPressed

        String[] columna = {"Nombre", "Precio", "Descripcion"};
        String[] registros = new String[3];
        TablaC.getTableHeader().setReorderingAllowed(false);
        DefaultTableModel dtm = new DefaultTableModel(null, columna) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String ssql = "SELECT * FROM servicios WHERE Nombre LIKE '%" + BuscarTodoC.getText() + "%' "
                + "OR Precio LIKE '%" + BuscarTodoC.getText() + "%'"
                + "OR Descripcion LIKE '%" + BuscarTodoC.getText() + "%'";      
        try {
            Connection cn = Conexion.conectarBD();
            Statement stm = (Statement) cn.createStatement();
            ResultSet res = stm.executeQuery(ssql);

            while (res.next()) {

                registros[0] = res.getString(1); //nombre
                registros[1] = res.getString(2); //precio
                registros[2] = res.getString(3); //descripcion

                dtm.addRow(registros);
            }
            TablaC.setModel(dtm);
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error al filtrar desde la BD " + e);
            JOptionPane.showMessageDialog(null, "¡Contacte al administrador!");
        }
    }//GEN-LAST:event_BuscarTodoCKeyPressed

