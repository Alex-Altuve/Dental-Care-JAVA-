    private void NumTransKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NumTransKeyTyped
        int key = evt.getKeyChar();
        boolean numeros = (key >= 48 && key <= 57) || key==127 || key==8;

        if (!numeros)
        {
            Error_NumTrans.setText("No se aceptan caracteres especiales ni letras");
            evt.consume();
        }
        if (NumTrans.getText().trim().length() >=9) {

            Error_NumTrans.setText("El N° de transferencia debe tener 9 dígitos");
            evt.consume();
        }
    }//GEN-LAST:event_NumTransKeyTyped

